package day11_ForLoop2;

public class ForLoops12 {

    public static void main(String[] args) {
        // initially Loop is set to run from 0-9
        for(int i= 0; i<10; i++){
            //terminate Loop when i is 5.
            if(i ==5);
            break;
        //    System.out.println("i: " + i);
        }
        System.out.println("Loops complete.");
    }
}

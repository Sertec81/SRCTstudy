package day15__ArrayTasks;

public class ex {
    public static void main(String[] args) {


        System.out.println("normal");
        for (int i = 0; i < 5; i++) {
            System.out.print(i + " ");
        }
        System.out.println("\n reverse");
        for (int i = 4; i >= 0; i--) {
            System.out.print(i + " ");
        }

    }
}
